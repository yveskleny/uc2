numero_inteiro = int(input("Digite um número inteiro: "))
numero_real = float(input("Digite um número real: "))

print(f"Soma dos números é {(numero_inteiro + numero_real):.2f}")
print(f"Subtração dos número é {(numero_inteiro - numero_real):.2f}")
print(f"Multiplicação dos números é {(numero_inteiro * numero_real):.2f}")
print(f"Divisão dos números é {(numero_inteiro / numero_real):.2f}")
